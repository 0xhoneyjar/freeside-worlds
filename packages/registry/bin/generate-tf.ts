#!/usr/bin/env bun
/**
 * generate-tf.ts — registry YAML → terraform emission
 *
 * Reads worlds/<slug>.yaml and emits two terraform files into tf-out/:
 *   - world-<slug>.tf         calls modules/world with the manifest's inputs
 *   - world-<slug>-secrets.tf declares aws_secretsmanager_secret structure
 *
 * Output is canonical-format. First migration into loa-freeside accepts
 * whitespace + comment churn; thereafter the generator is the source-of-truth
 * and any drift is a bug.
 *
 * Usage:
 *   bun bin/generate-tf.ts                generate all worlds
 *   bun bin/generate-tf.ts <slug>         generate one world
 */
import { readFileSync, readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { parse as parseYaml } from "yaml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const WORLDS_DIR = resolve(ROOT, "worlds");
const OUT_DIR = resolve(ROOT, "tf-out");

mkdirSync(OUT_DIR, { recursive: true });

type SecretDecl = {
  key: string;
  env_var?: string;
  lifecycle?: "build" | "deploy" | "runtime";
  description?: string;
};

type WorldManifest = {
  schema_version: string;
  slug: string;
  name: string;
  repo: string;
  description?: string;
  hosting?: {
    type: string;
    cpu?: number;
    memory?: number;
    port?: number;
    desired_count?: number;
    health_check_path?: string;
    finn_url?: string;
  };
  identity?: Array<{ type: string; cookie_domain?: string }>;
  env_vars?: Record<string, string>;
  secrets?: SecretDecl[];
  compose_with?: Array<{ slug: string; relationship: string }>;
  network?: {
    extra_egress_rules?: SecurityGroupRule[];
    extra_ingress_rules?: SecurityGroupRule[];
  };
  log_retention_days?: number;
  image_tag?: string;
};

type SecurityGroupRule = {
  protocol: string;
  from_port: number;
  to_port: number;
  cidr_blocks?: string[];
  source_security_group_ref?: string;
  description: string;
};

const filter = process.argv[2];
const files = readdirSync(WORLDS_DIR)
  .filter((f) => f.endsWith(".yaml"))
  .filter((f) => !filter || f === `${filter}.yaml`)
  .sort();

if (files.length === 0) {
  console.error(`no world YAMLs found${filter ? ` matching '${filter}'` : ""}`);
  process.exit(1);
}

for (const file of files) {
  const path = join(WORLDS_DIR, file);
  const manifest = parseYaml(readFileSync(path, "utf-8")) as WorldManifest;
  const moduleName = `world_${manifest.slug.replace(/-/g, "_")}`;

  // Emit world-{slug}.tf
  const tfPath = join(OUT_DIR, `world-${manifest.slug}.tf`);
  writeFileSync(tfPath, renderWorldTf(manifest, moduleName));

  // Emit world-{slug}-secrets.tf if any runtime secrets
  const runtimeSecrets = (manifest.secrets ?? []).filter(
    (s) => (s.lifecycle ?? "runtime") === "runtime",
  );
  if (runtimeSecrets.length > 0) {
    const secretsPath = join(OUT_DIR, `world-${manifest.slug}-secrets.tf`);
    writeFileSync(secretsPath, renderSecretsTf(manifest, runtimeSecrets));
  }

  console.log(`✓ ${manifest.slug}: ${tfPath}${runtimeSecrets.length > 0 ? " + secrets" : ""}`);
}

console.log(`\n${files.length} world${files.length === 1 ? "" : "s"} → ${OUT_DIR}`);

// ---------------------------------------------------------------------------
// Renderers
// ---------------------------------------------------------------------------

function renderWorldTf(m: WorldManifest, moduleName: string): string {
  const h = m.hosting ?? { type: "ECSHosting" };
  const out: string[] = [];

  // Header banner
  out.push(`# =============================================================================`);
  out.push(`# World: ${m.slug}${m.description ? ` — ${stripBannerNewlines(m.description)}` : ""}`);
  out.push(`# =============================================================================`);
  out.push(`# Auto-generated by freeside-worlds/packages/registry/bin/generate-tf.ts.`);
  out.push(`# Source-of-truth: freeside-worlds/packages/registry/worlds/${m.slug}.yaml`);
  out.push(`# Do not hand-edit; modify the YAML and re-run the generator.`);
  out.push(`# =============================================================================`);
  out.push("");

  // module block
  out.push(`module "${moduleName}" {`);
  out.push(`  source = "./modules/world"`);
  out.push("");
  out.push(`  name        = "${m.slug}"`);
  out.push(`  repo        = "${m.repo}"`);
  out.push(`  environment = var.environment`);
  out.push("");
  out.push(`  # Shared infrastructure references`);
  out.push(`  cluster_id               = aws_ecs_cluster.main.id`);
  out.push(`  cluster_name             = aws_ecs_cluster.main.name`);
  out.push(`  vpc_id                   = module.vpc.vpc_id`);
  out.push(`  private_subnets          = module.vpc.private_subnets`);
  out.push(`  alb_listener_arn         = aws_lb_listener.https.arn`);
  out.push(`  alb_security_group_id    = aws_security_group.alb.id`);
  out.push(`  efs_file_system_id       = aws_efs_file_system.worlds.id`);
  out.push(`  efs_security_group_id    = aws_security_group.worlds_efs.id`);
  out.push(`  github_oidc_provider_arn = aws_iam_openid_connect_provider.github.arn`);
  out.push(`  kms_key_arn              = aws_kms_key.secrets.arn`);
  out.push(`  name_prefix              = local.name_prefix`);
  out.push(`  common_tags              = local.common_tags`);
  out.push(`  aws_region               = var.aws_region`);
  out.push(`  account_id               = data.aws_caller_identity.current.account_id`);
  out.push("");

  // Compute (only emit non-defaults)
  const hostingLines: string[] = [];
  if (h.cpu !== undefined && h.cpu !== 256) hostingLines.push(`  cpu    = ${h.cpu}`);
  if (h.memory !== undefined && h.memory !== 512) hostingLines.push(`  memory = ${h.memory}`);
  if (hostingLines.length === 0) {
    // Always emit cpu/memory even at defaults for explicit canonical form
    hostingLines.push(`  cpu    = ${h.cpu ?? 256}`);
    hostingLines.push(`  memory = ${h.memory ?? 512}`);
  }
  out.push(...hostingLines);

  if (h.port !== undefined && h.port !== 3000) {
    out.push(`  port = ${h.port}`);
  }
  if (h.health_check_path !== undefined && h.health_check_path !== "/") {
    out.push(`  health_check_path = "${h.health_check_path}"`);
  }
  if (h.desired_count !== undefined && h.desired_count !== 1) {
    out.push(`  desired_count = ${h.desired_count}`);
  }

  // env_vars
  if (m.env_vars && Object.keys(m.env_vars).length > 0) {
    out.push("");
    out.push(`  env_vars = {`);
    for (const [k, v] of Object.entries(m.env_vars)) {
      out.push(`    ${k} = "${v}"`);
    }
    out.push(`  }`);
  }

  // secrets (only the runtime ones — the secrets={} map for ECS resolution)
  const runtimeSecrets = (m.secrets ?? []).filter(
    (s) => (s.lifecycle ?? "runtime") === "runtime",
  );
  if (runtimeSecrets.length > 0) {
    out.push("");
    out.push(`  secrets = {`);
    const maxEnvLen = Math.max(...runtimeSecrets.map((s) => (s.env_var ?? s.key.toUpperCase()).length));
    for (const s of runtimeSecrets) {
      const env = s.env_var ?? s.key.toUpperCase();
      const pad = " ".repeat(maxEnvLen - env.length);
      out.push(`    ${env}${pad} = aws_secretsmanager_secret.${m.slug.replace(/-/g, "_")}["${s.key}"].arn`);
    }
    out.push(`  }`);
    out.push("");
    out.push(`  secret_arns = [for s in aws_secretsmanager_secret.${m.slug.replace(/-/g, "_")} : s.arn]`);
  }

  // log_retention / image_tag (rare overrides)
  if (m.log_retention_days !== undefined && m.log_retention_days !== 30) {
    out.push("");
    out.push(`  log_retention_days = ${m.log_retention_days}`);
  }
  if (m.image_tag !== undefined && m.image_tag !== "latest") {
    out.push(`  image_tag = "${m.image_tag}"`);
  }

  out.push(`}`);

  // network extras
  const egress = m.network?.extra_egress_rules ?? [];
  const ingress = m.network?.extra_ingress_rules ?? [];
  if (egress.length > 0 || ingress.length > 0) {
    out.push("");
    let ruleIdx = 0;
    for (const rule of egress) {
      out.push("");
      out.push(renderSecurityGroupRule(m.slug, moduleName, "egress", rule, ruleIdx++));
    }
    for (const rule of ingress) {
      out.push("");
      out.push(renderSecurityGroupRule(m.slug, moduleName, "ingress", rule, ruleIdx++));
    }
  }

  // Outputs
  out.push("");
  out.push(`output "${m.slug.replace(/-/g, "_")}_ecr_url" {`);
  out.push(`  value       = module.${moduleName}.ecr_repository_url`);
  out.push(`  description = "ECR repository for ${m.slug} image pushes (CI uses this)"`);
  out.push(`}`);
  out.push("");
  out.push(`output "${m.slug.replace(/-/g, "_")}_ci_role_arn" {`);
  out.push(`  value       = module.${moduleName}.ci_deploy_role_arn`);
  out.push(`  description = "IAM role ARN for ${m.repo} GitHub Actions OIDC (set as AWS_DEPLOY_ROLE_ARN repo secret)"`);
  out.push(`}`);
  out.push("");
  out.push(`output "${m.slug.replace(/-/g, "_")}_subdomain" {`);
  out.push(`  value       = module.${moduleName}.subdomain`);
  out.push(`  description = "Subdomain (${m.slug}.{domain}) where the world is reachable"`);
  out.push(`}`);
  out.push("");

  return out.join("\n");
}

function renderSecurityGroupRule(
  slug: string,
  moduleName: string,
  type: "egress" | "ingress",
  rule: SecurityGroupRule,
  idx: number,
): string {
  const slugSnake = slug.replace(/-/g, "_");
  const resourceName =
    rule.from_port === rule.to_port
      ? `${slugSnake}_${type}_${rule.from_port}_${idx}`
      : `${slugSnake}_${type}_${rule.from_port}_${rule.to_port}_${idx}`;

  const lines: string[] = [];
  lines.push(`resource "aws_security_group_rule" "${resourceName}" {`);
  lines.push(`  type              = "${type}"`);
  lines.push(`  from_port         = ${rule.from_port}`);
  lines.push(`  to_port           = ${rule.to_port}`);
  lines.push(`  protocol          = "${rule.protocol}"`);
  if (rule.cidr_blocks) {
    lines.push(`  cidr_blocks       = ${JSON.stringify(rule.cidr_blocks)}`);
  }
  if (rule.source_security_group_ref) {
    lines.push(`  source_security_group_id = ${rule.source_security_group_ref}`);
  }
  if (type === "egress") {
    lines.push(`  security_group_id = module.${moduleName}.security_group_id`);
  } else {
    // For ingress on the SOURCE SG (e.g. RDS receiving from the world)
    if (rule.source_security_group_ref) {
      lines.push(`  security_group_id = ${rule.source_security_group_ref}`);
      // and source SG is the world's
      lines.pop();
      lines.push(`  source_security_group_id = module.${moduleName}.security_group_id`);
      lines.push(`  security_group_id        = ${rule.source_security_group_ref}`);
    } else {
      lines.push(`  security_group_id = module.${moduleName}.security_group_id`);
    }
  }
  lines.push(`  description       = "${rule.description}"`);
  lines.push(`}`);
  return lines.join("\n");
}

function renderSecretsTf(m: WorldManifest, secrets: SecretDecl[]): string {
  const slugSnake = m.slug.replace(/-/g, "_");
  const out: string[] = [];

  out.push(`# =============================================================================`);
  out.push(`# ${m.name} (${m.repo}) — AWS Secrets Manager structure`);
  out.push(`# =============================================================================`);
  out.push(`# Auto-generated by freeside-worlds/packages/registry/bin/generate-tf.ts.`);
  out.push(`# Source-of-truth: freeside-worlds/packages/registry/worlds/${m.slug}.yaml`);
  out.push(`#`);
  out.push(`# Defines the ${secrets.length} runtime secret${secrets.length === 1 ? "" : "s"} the ${m.slug} ECS task resolves at start.`);
  out.push(`# Values are NOT populated by terraform — load via scripts/load-${m.slug}-secrets.sh.`);
  out.push(`# =============================================================================`);
  out.push("");

  out.push(`locals {`);
  out.push(`  ${slugSnake}_secrets = toset([`);
  for (const s of secrets) {
    const comment = s.description ? `  # ${s.description}` : "";
    out.push(`    "${s.key}",${comment}`);
  }
  out.push(`  ])`);
  out.push(`}`);
  out.push("");

  out.push(`resource "aws_secretsmanager_secret" "${slugSnake}" {`);
  out.push(`  for_each = local.${slugSnake}_secrets`);
  out.push("");
  out.push(`  name        = "\${local.name_prefix}/worlds/${m.slug}/\${each.key}"`);
  out.push(`  description = "${m.name} runtime secret — loaded via scripts/load-${m.slug}-secrets.sh"`);
  out.push(`  kms_key_id  = aws_kms_key.secrets.arn`);
  out.push("");
  out.push(`  tags = merge(local.common_tags, {`);
  out.push(`    World   = "${m.slug}"`);
  out.push(`    Purpose = each.key`);
  out.push(`  })`);
  out.push("");
  out.push(`  recovery_window_in_days = 7`);
  out.push(`}`);
  out.push("");

  return out.join("\n");
}

function stripBannerNewlines(s: string): string {
  return s.replace(/\n+/g, " ").replace(/\s+/g, " ").trim();
}
